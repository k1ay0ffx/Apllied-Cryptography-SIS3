import java.math.BigInteger;

/**
 * IRSAEngine — интерфейс для RSA-движка.
 *
 * ╔══════════════════════════════════════════════════════════╗
 * ║  ДРУГОЙ РАЗРАБОТЧИК: реализуй этот интерфейс             ║
 * ║                                                          ║
 * ║  Создай класс RSAEngine implements IRSAEngine            ║
 * ║  и реализуй все методы ниже.                             ║
 * ║                                                          ║
 * ║  После этого в Main.java замени:                         ║
 * ║    IRSAEngine engine = new RSAEngineStub();              ║
 * ║  на:                                                     ║
 * ║    IRSAEngine engine = new RSAEngine();                  ║
 * ╚══════════════════════════════════════════════════════════╝
 */
public interface IRSAEngine {

    // ── Проверка простоты ─────────────────────────────────────────────────────

    /**
     * Проверяет является ли число простым.
     *
     * Реализовать: тест Миллера-Рабина (Miller-Rabin) с заданным
     * количеством раундов. Требования из задания:
     * - Корректно определяет малые простые (17, 31, 127)
     * - Отклоняет составные числа (15, 21, 91)
     * - Обрабатывает числа Кармайкла (561, 1105, 1729)
     * - Работает с 512-битными и 1024-битными числами
     *
     * @param n       число для проверки
     * @param rounds  количество раундов (рекомендуется ≥ 20 для криптографии)
     * @return true если число вероятно простое
     */
    boolean isPrime(BigInteger n, int rounds);

    /**
     * Генерирует случайное простое число заданного размера.
     *
     * Реализовать: генерация случайного нечётного числа нужного размера,
     * проверка тестом Миллера-Рабина, повтор до нахождения простого.
     *
     * @param bits   размер в битах (например, 512, 1024)
     * @return простое число заданного размера
     */
    BigInteger generatePrime(int bits);

    // ── Модульная арифметика ──────────────────────────────────────────────────

    /**
     * Быстрое модульное возведение в степень.
     *
     * Реализовать: Square-and-Multiply алгоритм.
     * Формула: (base^exp) mod mod
     *
     * Используется для: RSA-шифрование (m^e mod n),
     * дешифрование (c^d mod n), подпись (m^d mod n).
     *
     * @param base  основание
     * @param exp   показатель степени
     * @param mod   модуль
     * @return (base^exp) mod mod
     */
    BigInteger modExp(BigInteger base, BigInteger exp, BigInteger mod);

    /**
     * Расширенный алгоритм Евклида.
     *
     * Реализовать: Extended GCD → находит x, y такие что a*x + b*y = gcd(a,b).
     * Используется для вычисления d = e⁻¹ mod φ(n).
     *
     * @param a  первое число
     * @param b  второе число
     * @return массив [gcd, x, y] где a*x + b*y = gcd
     */
    BigInteger[] extendedGCD(BigInteger a, BigInteger b);

    /**
     * Модульный мультипликативный обратный элемент.
     *
     * Реализовать: a⁻¹ mod m используя extendedGCD.
     * Используется для: d = e⁻¹ mod λ(n)
     *
     * @param a  число для инвертирования
     * @param m  модуль
     * @return a⁻¹ mod m
     * @throws ArithmeticException если обратный элемент не существует
     */
    BigInteger modInverse(BigInteger a, BigInteger m);

    // ── Генерация ключей ──────────────────────────────────────────────────────

    /**
     * Генерирует пару RSA ключей заданного размера.
     *
     * Реализовать:
     *   1. Генерировать два простых числа p и q размером bits/2
     *   2. n = p × q
     *   3. λ(n) = lcm(p-1, q-1)  [функция Кармайкла]
     *      или φ(n) = (p-1)×(q-1) [функция Эйлера]
     *   4. e = 65537
     *   5. d = e⁻¹ mod λ(n)
     *   6. Проверить что p ≠ q и |p-q| достаточно велико
     *
     * @param bits  размер ключа: 1024, 2048 или 4096 бит
     * @return сгенерированная пара ключей
     */
    RSAKeyPair generateKeyPair(int bits);

    /**
     * Проверяет корректность пары ключей.
     *
     * Реализовать: проверить что:
     *   - n = p × q (если p, q доступны)
     *   - e × d ≡ 1 mod λ(n)
     *   - p и q простые (тест Миллера-Рабина)
     *
     * @param keyPair  пара ключей для проверки
     * @return true если ключ корректен
     */
    boolean validateKeyPair(RSAKeyPair keyPair);

    // ── Шифрование / Дешифрование ─────────────────────────────────────────────

    /**
     * Шифрует сообщение с PKCS#1 v1.5 паддингом.
     *
     * Реализовать:
     *   1. Применить PKCS#1 v1.5 паддинг:
     *      0x00 || 0x02 || PS || 0x00 || message
     *      PS — случайные ненулевые байты
     *   2. Зашифровать: c = m^e mod n
     *
     * @param message    открытый текст
     * @param publicKey  публичный ключ получателя
     * @return зашифрованные байты
     */
    byte[] encryptPKCS1(byte[] message, RSAKeyPair publicKey);

    /**
     * Дешифрует сообщение с PKCS#1 v1.5 паддингом.
     *
     * Реализовать:
     *   1. Расшифровать: m = c^d mod n
     *   2. Проверить и удалить PKCS#1 v1.5 паддинг
     *
     * @param ciphertext  зашифрованные байты
     * @param privateKey  приватный ключ
     * @return расшифрованное сообщение
     */
    byte[] decryptPKCS1(byte[] ciphertext, RSAKeyPair privateKey);

    /**
     * Шифрует сообщение с OAEP паддингом (RSA-OAEP-SHA256).
     *
     * Реализовать:
     *   1. OAEP encode (используя SHA-256 и MGF1)
     *   2. Зашифровать: c = m^e mod n
     *
     * @param message    открытый текст
     * @param publicKey  публичный ключ получателя
     * @return зашифрованные байты
     */
    byte[] encryptOAEP(byte[] message, RSAKeyPair publicKey);

    /**
     * Дешифрует сообщение с OAEP паддингом.
     *
     * @param ciphertext  зашифрованные байты
     * @param privateKey  приватный ключ
     * @return расшифрованное сообщение
     */
    byte[] decryptOAEP(byte[] ciphertext, RSAKeyPair privateKey);
}